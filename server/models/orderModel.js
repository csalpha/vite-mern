import mongoose from "mongoose";

/*  Defines the schema for the Order model, which includes the 
    fields that make up the collection/document structure. */
const orderSchema = new mongoose.Schema(
  {
    // properties
    orderItems: [
      {
        slug: { type: String, required: true },
        name: { type: String, required: true },
        quantity: { type: Number, required: true },
        image: { type: String, required: true },
        price: { type: Number, required: true },
        product: {
          type: mongoose.Schema.Types.ObjectId,
          ref: "Product",
          required: true,
        },
      },
    ],
    shippingAddress: {
      fullName: { type: String, required: true },
      address: { type: String, required: true },
      city: { type: String, required: true },
      postalCode: { type: String, required: true },
      country: { type: String, required: true },
      lat: Number,
      lng: Number,
    },
    paymentMethod: { type: String, required: true },
    paymentResult: {
      id: String,
      status: String,
      update_time: String,
      email_address: String,
    },
    itemsPrice: { type: Number, required: true },
    shippingPrice: { type: Number, required: true },
    taxPrice: { type: Number, required: true },
    totalPrice: { type: Number, required: true },
    // A reference to the User model's ObjectId
    user: { type: mongoose.Schema.Types.ObjectId, ref: "User", required: true },
    // A reference to the User model's ObjectId
    seller: { type: mongoose.Schema.Types.ObjectID, ref: "User" },
    isPaid: { type: Boolean, default: false },
    paidAt: { type: Date },
    isDelivered: { type: Boolean, default: false },
    deliveredAt: { type: Date },
  },
  {
    timestamps: true,
  }
);

// Compiles the schema into a Mongoose model named "Order".
const Order = mongoose.model("Order", orderSchema);

// Exports the model so that it can be imported and used in other files.
export default Order;
